        Purchase.setTicket(tiket);
        Purchase.setTicketType(jenisTiket);
        Purchase.setTicketPrice(harga);
        Purchase.setKuota(kuota);
        
        Purchase.Ticket_Txt.setText(Purchase.getTicket());
        Purchase.TicketType_Txt.setText(Purchase.getTicketType());
        Purchase.TicketPrice_Txt.setText(Purchase.getTicketPrice());
        Purchase.Kuota_Txt.setText(Purchase.getKuota());
        
        String TicketCode = Purchase.Ticket_Txt.getText();
        Purchase.generatePurchaseCode(TicketCode.substring(TicketCode.length() - 1));
